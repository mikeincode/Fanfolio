export function getAssetById(id: string): Asset | undefined {
  return MOCK_ASSETS.find(a => a.id === id);
}

export function getAssetsByType(type: AssetType | "All"): Asset[] {
  if (type === "All") return MOCK_ASSETS;
  return MOCK_ASSETS.filter(a => a.type === type);
}
